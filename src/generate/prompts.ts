/**
 * プロンプトテンプレート（§5）。
 *
 * 共通要件（リライト/新規とも）:
 *  - 薬機法・景表法に抵触する断定的効能表現を禁止（「治る」「必ず痩せる」「効く」等）。
 *    「サポート」「期待できる」等に留め、医療アドバイスにしない。
 *  - 事実・数値・商品情報は捏造しない。リライトでは元記事にある情報のみ使用。
 *    新規記事では事実確認が必要な箇所は 【要確認: ○○】 のプレースホルダーにする。
 *  - AI検索での引用されやすさ: 結論先出し、見出し直下に要点、Q&A構造の活用。
 *
 * リライト（5-1）: 元記事の構成・トーンを尊重し全書き換えせず改善に徹する。
 *   R1はタイトル案を3つ提示。出力=本文HTML + 変更点サマリー。
 */
import type { Candidate } from "../analyze/types.ts";

export const COMPLIANCE_GUIDE = `あなたはリカバリーウェア専門メディアのSEO編集者です。以下を厳守してください。
【表現ルール（薬機法・景表法／必ず遵守）】
- 医薬品的な効能効果の断定表現は禁止（「治る」「治療」「必ず痩せる」「効く」「改善する」等）。
- 「サポートが期待できる」「〜と言われています」等の非断定的表現に留める。医療アドバイスにしない。
- 事実・数値・商品スペック・価格・比較データは、入力（元記事）に無いものを創作しない。
- 誇大・優良誤認となる比較や最上級表現（「No.1」「最強」等）は根拠が元記事に無ければ使わない。
【AI検索での引用されやすさ】
- 結論を先出しし、各見出し直下に要点を1-2文で置く。可能な範囲でQ&A（FAQ）構造を活用する。`;

/** リライト（§5-1）用ユーザープロンプト。元記事を尊重し改善に徹する。 */
export function buildRewritePrompt(c: Candidate, originalTitle: string, originalHtml: string): string {
  const m = c.metrics;
  const metrics = [
    m.position !== undefined ? `平均掲載順位 ${m.position.toFixed(1)}位` : null,
    m.ctr !== undefined ? `CTR ${(m.ctr * 100).toFixed(1)}%` : null,
    m.impressions !== undefined ? `インプレッション ${m.impressions}` : null,
    m.clicks !== undefined ? `クリック ${m.clicks}` : null,
  ].filter(Boolean).join(" / ");

  return `既存記事を「リライト」して改善してください。改善タイプは ${c.rule} です。

## 改善の狙い（${c.rule}）
${c.rule === "R1" ? "検索上位に表示できているのにCTRが低い。タイトル・導入・見出し・メタ的要素を磨いてクリック率を上げる。本文の大幅な作り替えはしない。" : ""}${c.rule === "R3" ? "2ページ目付近。網羅性・見出し構成・要点提示を改善して1ページ目を狙う。元記事の主旨は維持。" : ""}${c.rule === "R2" ? "以前より順位が下落。情報の鮮度・網羅性・構成を見直して回復を狙う。元記事の主旨は維持。" : ""}

## 現状データ（GSC）
- 対象URL: ${c.targetUrl}
- ${metrics}
- 主な流入クエリ: ${c.queries.map((q) => `「${q}」`).join(" / ")}

## 元記事タイトル
${originalTitle}

## 元記事本文（HTML）
${originalHtml}

## 指示
- 元記事の構成・トーン・情報を尊重し、**全書き換えではなく改善**に徹する（見出しの意図や順序は極力維持）。
- 上記の流入クエリの検索意図に、より的確に応える（特にCTRを上げるための導入とタイトル）。
- 事実・数値・商品情報は元記事にあるもののみ使用。新しい事実主張・数値・比較を足さない。
- 表現ルール（薬機法・景表法）を厳守。
${c.rule === "R1" ? "- タイトル案を3つ提示する（クリックされやすく、かつ誇大にならない範囲で）。" : "- タイトル案を3つ提示する。"}

## 出力形式（**JSONのみ**を返す。前後に説明文やコードフェンスを付けない）
{
  "titleSuggestions": ["案1", "案2", "案3"],
  "changeSummary": "何をどう改善したかの箇条書き（プレーンテキスト、3〜6項目）",
  "contentHtml": "リライト後の本文HTML（<h2>等の見出し構造を含む。元記事の構成を尊重）"
}`;
}

/** 新規記事（§5-2）用ユーザープロンプト。事実確認が必要な箇所はプレースホルダーにする。 */
export function buildNewArticlePrompt(c: Candidate, internalLinkTitles: string[]): string {
  const links = internalLinkTitles.slice(0, 30).map((t) => `- ${t}`).join("\n");
  return `以下の検索需要に応える「新規記事」を書いてください（改善タイプ ${c.rule}）。

## 対象クエリ（検索需要）
${c.queries.map((q) => `「${q}」`).join(" / ")}
（想定インプレッション: ${c.metrics.impressions ?? "-"}）

## 指示
- 上記クエリの検索意図に正面から答える記事。結論先出し、見出し直下に要点、FAQ(Q&A)構造を活用。
- **商品の具体的スペック・価格・比較データ・数値は創作しない。** 事実確認が必要な箇所は必ず
  【要確認: ○○】 の形のプレースホルダーにして、人間レビューに委ねる（例:【要確認: BAKUNEの価格】）。
- 表現ルール（薬機法・景表法）を厳守。断定的効能表現は禁止。
- 文字数目安 3000〜5000字。見出し(h2/h3)構造を付ける。
- 内部リンク候補（サイト内関連記事タイトル。関連するものがあれば本文中で自然に言及してよいが、URLは不明なので【要確認: 内部リンク先URL】とする）:
${links || "（なし）"}

## 出力形式（**JSONのみ**を返す。前後に説明文やコードフェンスを付けない）
{
  "title": "記事タイトル案",
  "metaDescription": "メタディスクリプション案（120字程度）",
  "contentHtml": "本文HTML（<h2>等の見出し構造付き。事実未確認箇所は【要確認: ○○】）"
}`;
}
